-- ==========================================
-- 6. РАСПИСАНИЕ (SCHEDULE) И ОГРАНИЧЕНИЯ (CONSTRAINTS)
-- ==========================================

CREATE TABLE schedule (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    group_id INT NOT NULL REFERENCES groups(id) ON DELETE CASCADE,
    teacher_id INT NOT NULL REFERENCES teachers(id) ON DELETE CASCADE,
    subject_id INT NOT NULL REFERENCES subjects(id) ON DELETE CASCADE,
    classroom_id INT NOT NULL REFERENCES classrooms(id) ON DELETE CASCADE,
    day_id INT NOT NULL REFERENCES days_of_week(id) ON DELETE CASCADE,
    slot_id INT NOT NULL REFERENCES time_slots(id) ON DELETE CASCADE,
    -- Физические ограничения для исключения накладок:
    -- 1. Учитель не может быть в двух местах одновременно
    CONSTRAINT uq_schedule_teacher_day_slot UNIQUE (teacher_id, day_id, slot_id),
    -- 2. Группа не может быть в двух местах одновременно
    CONSTRAINT uq_schedule_group_day_slot UNIQUE (group_id, day_id, slot_id),
    -- 3. Кабинет не может быть занят двумя группами одновременно
    CONSTRAINT uq_schedule_classroom_day_slot UNIQUE (classroom_id, day_id, slot_id)
);

CREATE INDEX idx_schedule_group_id ON schedule(group_id);
CREATE INDEX idx_schedule_teacher_id ON schedule(teacher_id);
CREATE INDEX idx_schedule_classroom_id ON schedule(classroom_id);
CREATE INDEX idx_schedule_day_id ON schedule(day_id);

-- Правила: у группы в каждый учебный день с расписанием ровно 3–4 пары;
-- у групп 1–2 курса время начала первой пары дня — с 08:00 по 09:35 (по справочнику time_slots).
CREATE OR REPLACE FUNCTION schedule_validate_rules()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
    IF EXISTS (
        SELECT 1
        FROM schedule s
        GROUP BY s.group_id, s.day_id
        HAVING COUNT(*) < 3 OR COUNT(*) > 4
    ) THEN
        RAISE EXCEPTION 'schedule: у группы в учебный день должно быть от 3 до 4 пар.';
    END IF;

    IF EXISTS (
        SELECT 1
        FROM schedule s
        JOIN groups g ON g.id = s.group_id
        JOIN time_slots ts ON ts.id = s.slot_id
        WHERE g.course_id IN (1, 2)
        GROUP BY s.group_id, s.day_id
        HAVING MIN(ts.start_time) < TIME '08:00'
            OR MIN(ts.start_time) > TIME '09:35'
    ) THEN
        RAISE EXCEPTION 'schedule: для групп 1–2 курса первая пара дня должна начинаться с 08:00 до 09:35.';
    END IF;

    RETURN NULL;
END;
$$;

DROP TRIGGER IF EXISTS trg_schedule_validate_rules ON schedule;
CREATE CONSTRAINT TRIGGER trg_schedule_validate_rules
AFTER INSERT OR UPDATE OR DELETE ON schedule
DEFERRABLE INITIALLY DEFERRED
FOR EACH ROW
EXECUTE PROCEDURE schedule_validate_rules();

