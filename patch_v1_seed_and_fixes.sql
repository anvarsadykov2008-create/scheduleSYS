  64,   -- 4 × 16 недель
  t.teacher_id,
  TRUE
FROM ap, lt_pe lp, pe_subj ps,
     grp_pe gp
JOIN teachers t ON t.employee_code = gp.emp_code
ON CONFLICT DO NOTHING;

