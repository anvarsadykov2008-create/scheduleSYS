WITH
  ap  AS (SELECT academic_period_id FROM academic_periods WHERE code='2025-2026-2'),
  g   AS (SELECT group_id, code FROM groups),
  s   AS (SELECT subject_id, name FROM subjects),
  t   AS (SELECT teacher_id, employee_code FROM teachers),
  lt  AS (SELECT lesson_type_id, code FROM lesson_types)
