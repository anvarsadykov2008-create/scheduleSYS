  -- Назначаем преподавателей физкультуры группам по курсу
  grp_pe AS (
    SELECT g.group_id, g.course_no,
      CASE (ROW_NUMBER() OVER (ORDER BY g.group_id) % 5)
        WHEN 0 THEN 'T001'
        WHEN 1 THEN 'T026'
        WHEN 2 THEN 'T030'
        WHEN 3 THEN 'T035'
        ELSE        'T050'
      END AS emp_code
    FROM groups g
  )
