BEGIN;

-- =========================================================
-- Production-ready schema for a college timetable generator
-- =========================================================

-- Drop in reverse dependency order
DROP TABLE IF EXISTS schedule;
DROP TABLE IF EXISTS schedule_conflicts_log;
DROP TABLE IF EXISTS schedule_generation_runs;
DROP TABLE IF EXISTS group_subject_load;
DROP TABLE IF EXISTS room_unavailability;
DROP TABLE IF EXISTS group_unavailability;
DROP TABLE IF EXISTS teacher_unavailability;
DROP TABLE IF EXISTS subject_room_type;
DROP TABLE IF EXISTS subject_lesson_types;
DROP TABLE IF EXISTS teacher_subjects;
DROP TABLE IF EXISTS teacher_load;
DROP TABLE IF EXISTS teacher_preferences;
DROP TABLE IF EXISTS rooms;
DROP TABLE IF EXISTS group_subgroups;
DROP TABLE IF EXISTS groups;
DROP TABLE IF EXISTS subjects;
DROP TABLE IF EXISTS teachers;
DROP TABLE IF EXISTS time_slots;
DROP TABLE IF EXISTS room_types;
DROP TABLE IF EXISTS lesson_types;
DROP TABLE IF EXISTS academic_periods;
DROP TABLE IF EXISTS specialties;
DROP TABLE IF EXISTS departments;

-- =========================================================
-- Reference catalogs
-- =========================================================

CREATE TABLE departments (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code VARCHAR(30) NOT NULL,
    name VARCHAR(200) NOT NULL,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_departments_code UNIQUE (code),
    CONSTRAINT uq_departments_name UNIQUE (name),
    CONSTRAINT chk_departments_code_not_blank CHECK (BTRIM(code) <> ''),
    CONSTRAINT chk_departments_name_not_blank CHECK (BTRIM(name) <> '')
);

CREATE TABLE specialties (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    department_id BIGINT NOT NULL REFERENCES departments(id) ON DELETE RESTRICT,
    code VARCHAR(30) NOT NULL,
    name VARCHAR(200) NOT NULL,
    qualification VARCHAR(100),
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_specialties_department_code UNIQUE (department_id, code),
    CONSTRAINT uq_specialties_department_name UNIQUE (department_id, name),
    CONSTRAINT chk_specialties_code_not_blank CHECK (BTRIM(code) <> ''),
    CONSTRAINT chk_specialties_name_not_blank CHECK (BTRIM(name) <> '')
);

CREATE TABLE academic_periods (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code VARCHAR(50) NOT NULL,
    name VARCHAR(200) NOT NULL,
    academic_year VARCHAR(20) NOT NULL,
    term_no SMALLINT NOT NULL,
    start_date DATE NOT NULL,
    end_date DATE NOT NULL,
    weeks_in_period NUMERIC(6,2) NOT NULL,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_academic_periods_code UNIQUE (code),
    CONSTRAINT uq_academic_periods_year_term UNIQUE (academic_year, term_no),
    CONSTRAINT chk_academic_periods_code_not_blank CHECK (BTRIM(code) <> ''),
    CONSTRAINT chk_academic_periods_name_not_blank CHECK (BTRIM(name) <> ''),
    CONSTRAINT chk_academic_periods_dates CHECK (end_date >= start_date),
    CONSTRAINT chk_academic_periods_weeks CHECK (weeks_in_period > 0),
    CONSTRAINT chk_academic_periods_term CHECK (term_no BETWEEN 1 AND 12)
);

CREATE TABLE lesson_types (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code VARCHAR(20) NOT NULL,
    name VARCHAR(100) NOT NULL,
    is_lab BOOLEAN NOT NULL DEFAULT FALSE,
    requires_room_match BOOLEAN NOT NULL DEFAULT TRUE,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_lesson_types_code UNIQUE (code),
    CONSTRAINT uq_lesson_types_name UNIQUE (name),
    CONSTRAINT chk_lesson_types_code_not_blank CHECK (BTRIM(code) <> ''),
    CONSTRAINT chk_lesson_types_name_not_blank CHECK (BTRIM(name) <> '')
);

CREATE TABLE room_types (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code VARCHAR(30) NOT NULL,
    name VARCHAR(100) NOT NULL,
    is_lab BOOLEAN NOT NULL DEFAULT FALSE,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    notes TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_room_types_code UNIQUE (code),
    CONSTRAINT uq_room_types_name UNIQUE (name),
    CONSTRAINT chk_room_types_code_not_blank CHECK (BTRIM(code) <> ''),
    CONSTRAINT chk_room_types_name_not_blank CHECK (BTRIM(name) <> '')
);

CREATE TABLE time_slots (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    day_of_week SMALLINT NOT NULL,
    slot_number SMALLINT NOT NULL,
    start_time TIME NOT NULL,
    end_time TIME NOT NULL,
    academic_hours NUMERIC(5,2) NOT NULL DEFAULT 2.00,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_time_slots_day_slot UNIQUE (day_of_week, slot_number),
    CONSTRAINT uq_time_slots_day_time UNIQUE (day_of_week, start_time, end_time),
    CONSTRAINT chk_time_slots_day CHECK (day_of_week BETWEEN 1 AND 7),
    CONSTRAINT chk_time_slots_slot CHECK (slot_number BETWEEN 1 AND 12),
    CONSTRAINT chk_time_slots_time CHECK (end_time > start_time),
    CONSTRAINT chk_time_slots_academic_hours CHECK (academic_hours > 0)
);

-- =========================================================
-- Core entities
-- =========================================================

CREATE TABLE teachers (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_code VARCHAR(50),
    last_name VARCHAR(100) NOT NULL,
    first_name VARCHAR(100) NOT NULL,
    middle_name VARCHAR(100),
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    default_monthly_hour_limit NUMERIC(7,2),
    default_weekly_hour_limit NUMERIC(6,2),
    default_daily_hour_limit NUMERIC(6,2),
    default_daily_lessons_limit SMALLINT,
    notes TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_teachers_employee_code UNIQUE (employee_code),
    CONSTRAINT chk_teachers_last_name_not_blank CHECK (BTRIM(last_name) <> ''),
    CONSTRAINT chk_teachers_first_name_not_blank CHECK (BTRIM(first_name) <> ''),
    CONSTRAINT chk_teachers_monthly_limit CHECK (default_monthly_hour_limit IS NULL OR default_monthly_hour_limit > 0),
    CONSTRAINT chk_teachers_weekly_limit CHECK (default_weekly_hour_limit IS NULL OR default_weekly_hour_limit > 0),
    CONSTRAINT chk_teachers_daily_hour_limit CHECK (default_daily_hour_limit IS NULL OR default_daily_hour_limit > 0),
    CONSTRAINT chk_teachers_daily_lesson_limit CHECK (default_daily_lessons_limit IS NULL OR default_daily_lessons_limit > 0)
);

CREATE UNIQUE INDEX uq_teachers_fio
    ON teachers (last_name, first_name, COALESCE(middle_name, ''));

CREATE TABLE subjects (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code VARCHAR(50),
    name VARCHAR(200) NOT NULL,
    subject_kind VARCHAR(30) NOT NULL DEFAULT 'standard',
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    notes TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_subjects_code UNIQUE (code),
    CONSTRAINT uq_subjects_name UNIQUE (name),
    CONSTRAINT chk_subjects_name_not_blank CHECK (BTRIM(name) <> ''),
    CONSTRAINT chk_subjects_kind CHECK (subject_kind IN ('standard', 'module', 'elective', 'practicum', 'physical', 'general'))
);

CREATE TABLE groups (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    specialty_id BIGINT NOT NULL REFERENCES specialties(id) ON DELETE RESTRICT,
    code VARCHAR(50) NOT NULL,
    name VARCHAR(100) NOT NULL,
    course_no SMALLINT NOT NULL,
    student_count INTEGER,
    max_daily_lessons SMALLINT NOT NULL DEFAULT 4,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    notes TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_groups_code UNIQUE (code),
    CONSTRAINT chk_groups_code_not_blank CHECK (BTRIM(code) <> ''),
    CONSTRAINT chk_groups_name_not_blank CHECK (BTRIM(name) <> ''),
    CONSTRAINT chk_groups_course CHECK (course_no BETWEEN 1 AND 6),
    CONSTRAINT chk_groups_student_count CHECK (student_count IS NULL OR student_count > 0),
    CONSTRAINT chk_groups_max_daily_lessons CHECK (max_daily_lessons BETWEEN 1 AND 8)
);

CREATE TABLE group_subgroups (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    group_id BIGINT NOT NULL REFERENCES groups(id) ON DELETE CASCADE,
    code VARCHAR(20) NOT NULL,
    name VARCHAR(100) NOT NULL,
    student_count INTEGER,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_group_subgroups_group_code UNIQUE (group_id, code),
    CONSTRAINT uq_group_subgroups_group_subgroup UNIQUE (group_id, subgroup_id),
    CONSTRAINT chk_group_subgroups_code_not_blank CHECK (BTRIM(code) <> ''),
    CONSTRAINT chk_group_subgroups_name_not_blank CHECK (BTRIM(name) <> ''),
    CONSTRAINT chk_group_subgroups_student_count CHECK (student_count IS NULL OR student_count > 0)
);

CREATE TABLE rooms (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code VARCHAR(30) NOT NULL,
    name VARCHAR(100) NOT NULL,
    building VARCHAR(100) NOT NULL,
    floor SMALLINT,
    capacity INTEGER,
    room_type_id BIGINT NOT NULL REFERENCES room_types(id) ON DELETE RESTRICT,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    notes TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_rooms_building_code UNIQUE (building, code),
    CONSTRAINT chk_rooms_code_not_blank CHECK (BTRIM(code) <> ''),
    CONSTRAINT chk_rooms_name_not_blank CHECK (BTRIM(name) <> ''),
    CONSTRAINT chk_rooms_building_not_blank CHECK (BTRIM(building) <> ''),
    CONSTRAINT chk_rooms_floor CHECK (floor IS NULL OR floor >= 0),
    CONSTRAINT chk_rooms_capacity CHECK (capacity IS NULL OR capacity > 0)
);

-- =========================================================
-- Load, preferences and constraint tables
-- =========================================================

CREATE TABLE teacher_preferences (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    teacher_id BIGINT NOT NULL REFERENCES teachers(id) ON DELETE CASCADE,
    academic_period_id BIGINT NOT NULL REFERENCES academic_periods(id) ON DELETE CASCADE,
    time_slot_id BIGINT NOT NULL REFERENCES time_slots(id) ON DELETE CASCADE,
    preference_type VARCHAR(20) NOT NULL,
    weight SMALLINT NOT NULL DEFAULT 50,
    note TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_teacher_preferences UNIQUE (teacher_id, academic_period_id, time_slot_id, preference_type),
    CONSTRAINT chk_teacher_preferences_type CHECK (preference_type IN ('prefer', 'avoid')),
    CONSTRAINT chk_teacher_preferences_weight CHECK (weight BETWEEN 1 AND 100)
);

CREATE TABLE teacher_load (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    teacher_id BIGINT NOT NULL REFERENCES teachers(id) ON DELETE CASCADE,
    academic_period_id BIGINT NOT NULL REFERENCES academic_periods(id) ON DELETE CASCADE,
    max_monthly_hours NUMERIC(7,2),
    max_weekly_hours NUMERIC(6,2),
    max_daily_hours NUMERIC(6,2),
    max_daily_lessons SMALLINT,
    min_weekly_hours NUMERIC(6,2),
    notes TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_teacher_load_teacher_period UNIQUE (teacher_id, academic_period_id),
    CONSTRAINT chk_teacher_load_monthly CHECK (max_monthly_hours IS NULL OR max_monthly_hours > 0),
    CONSTRAINT chk_teacher_load_weekly CHECK (max_weekly_hours IS NULL OR max_weekly_hours > 0),
    CONSTRAINT chk_teacher_load_daily_hours CHECK (max_daily_hours IS NULL OR max_daily_hours > 0),
    CONSTRAINT chk_teacher_load_daily_lessons CHECK (max_daily_lessons IS NULL OR max_daily_lessons > 0),
    CONSTRAINT chk_teacher_load_min_weekly CHECK (min_weekly_hours IS NULL OR min_weekly_hours >= 0),
    CONSTRAINT chk_teacher_load_bounds CHECK (
        max_weekly_hours IS NULL
        OR min_weekly_hours IS NULL
        OR max_weekly_hours >= min_weekly_hours
    )
);

CREATE TABLE teacher_subjects (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    teacher_id BIGINT NOT NULL REFERENCES teachers(id) ON DELETE CASCADE,
    subject_id BIGINT NOT NULL REFERENCES subjects(id) ON DELETE CASCADE,
    lesson_type_id BIGINT REFERENCES lesson_types(id) ON DELETE RESTRICT,
    is_primary BOOLEAN NOT NULL DEFAULT FALSE,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    note TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE subject_lesson_types (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    subject_id BIGINT NOT NULL REFERENCES subjects(id) ON DELETE CASCADE,
    lesson_type_id BIGINT NOT NULL REFERENCES lesson_types(id) ON DELETE RESTRICT,
    is_default BOOLEAN NOT NULL DEFAULT FALSE,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    note TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_subject_lesson_types UNIQUE (subject_id, lesson_type_id)
);

CREATE TABLE subject_room_type (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    subject_id BIGINT NOT NULL REFERENCES subjects(id) ON DELETE CASCADE,
    room_type_id BIGINT NOT NULL REFERENCES room_types(id) ON DELETE RESTRICT,
    lesson_type_id BIGINT REFERENCES lesson_types(id) ON DELETE RESTRICT,
    priority SMALLINT NOT NULL DEFAULT 100,
    is_required BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT chk_subject_room_type_priority CHECK (priority BETWEEN 1 AND 1000)
);

CREATE TABLE teacher_unavailability (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    teacher_id BIGINT NOT NULL REFERENCES teachers(id) ON DELETE CASCADE,
    academic_period_id BIGINT NOT NULL REFERENCES academic_periods(id) ON DELETE CASCADE,
    time_slot_id BIGINT NOT NULL REFERENCES time_slots(id) ON DELETE CASCADE,
    reason TEXT,
    is_hard BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_teacher_unavailability UNIQUE (teacher_id, academic_period_id, time_slot_id)
);

CREATE TABLE group_unavailability (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    group_id BIGINT NOT NULL REFERENCES groups(id) ON DELETE CASCADE,
    subgroup_id BIGINT,
    academic_period_id BIGINT NOT NULL REFERENCES academic_periods(id) ON DELETE CASCADE,
    time_slot_id BIGINT NOT NULL REFERENCES time_slots(id) ON DELETE CASCADE,
    reason TEXT,
    is_hard BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT fk_group_unavailability_subgroup
        FOREIGN KEY (group_id, subgroup_id)
        REFERENCES group_subgroups(group_id, subgroup_id)
        ON DELETE CASCADE
);

CREATE TABLE room_unavailability (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    room_id BIGINT NOT NULL REFERENCES rooms(id) ON DELETE CASCADE,
    academic_period_id BIGINT NOT NULL REFERENCES academic_periods(id) ON DELETE CASCADE,
    time_slot_id BIGINT NOT NULL REFERENCES time_slots(id) ON DELETE CASCADE,
    reason TEXT,
    is_hard BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT uq_room_unavailability UNIQUE (room_id, academic_period_id, time_slot_id)
);

CREATE TABLE group_subject_load (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    academic_period_id BIGINT NOT NULL REFERENCES academic_periods(id) ON DELETE CASCADE,
    group_id BIGINT NOT NULL REFERENCES groups(id) ON DELETE CASCADE,
    subgroup_id BIGINT,
    subject_id BIGINT NOT NULL REFERENCES subjects(id) ON DELETE RESTRICT,
    lesson_type_id BIGINT NOT NULL REFERENCES lesson_types(id) ON DELETE RESTRICT,
    planned_weekly_hours NUMERIC(7,2) NOT NULL DEFAULT 0,
    total_hours NUMERIC(8,2) NOT NULL DEFAULT 0,
    min_sessions_per_week SMALLINT,
    max_sessions_per_week SMALLINT,
    preferred_room_type_id BIGINT REFERENCES room_types(id) ON DELETE RESTRICT,
    preferred_teacher_id BIGINT REFERENCES teachers(id) ON DELETE RESTRICT,
    is_mandatory BOOLEAN NOT NULL DEFAULT TRUE,
    notes TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT fk_group_subject_load_subgroup
        FOREIGN KEY (group_id, subgroup_id)
        REFERENCES group_subgroups(group_id, subgroup_id)
        ON DELETE CASCADE,
    CONSTRAINT chk_group_subject_load_hours CHECK (
        planned_weekly_hours >= 0
        AND total_hours >= 0
        AND (planned_weekly_hours > 0 OR total_hours > 0)
    ),
    CONSTRAINT chk_group_subject_load_min_sessions CHECK (min_sessions_per_week IS NULL OR min_sessions_per_week > 0),
    CONSTRAINT chk_group_subject_load_max_sessions CHECK (max_sessions_per_week IS NULL OR max_sessions_per_week > 0),
    CONSTRAINT chk_group_subject_load_session_bounds CHECK (
        max_sessions_per_week IS NULL
        OR min_sessions_per_week IS NULL
        OR max_sessions_per_week >= min_sessions_per_week
    )
);

-- =========================================================
-- Generation results and operational logs
-- =========================================================

CREATE TABLE schedule_generation_runs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    academic_period_id BIGINT NOT NULL REFERENCES academic_periods(id) ON DELETE RESTRICT,
    status VARCHAR(20) NOT NULL,
    requested_by VARCHAR(100),
    generator_version VARCHAR(50),
    parameters JSONB NOT NULL DEFAULT '{}'::JSONB,
    created_schedule_rows INTEGER NOT NULL DEFAULT 0,
    detected_conflicts INTEGER NOT NULL DEFAULT 0,
    requested_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    started_at TIMESTAMPTZ,
    finished_at TIMESTAMPTZ,
    notes TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT chk_schedule_generation_runs_status CHECK (status IN ('queued', 'running', 'completed', 'failed', 'cancelled')),
    CONSTRAINT chk_schedule_generation_runs_counts CHECK (created_schedule_rows >= 0 AND detected_conflicts >= 0),
    CONSTRAINT chk_schedule_generation_runs_time CHECK (
        finished_at IS NULL
        OR started_at IS NULL
        OR finished_at >= started_at
    )
);

CREATE TABLE schedule_conflicts_log (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    generation_run_id BIGINT REFERENCES schedule_generation_runs(id) ON DELETE SET NULL,
    academic_period_id BIGINT NOT NULL REFERENCES academic_periods(id) ON DELETE CASCADE,
    schedule_id BIGINT,
    conflict_type VARCHAR(50) NOT NULL,
    severity VARCHAR(20) NOT NULL,
    message TEXT NOT NULL,
    context JSONB NOT NULL DEFAULT '{}'::JSONB,
    teacher_id BIGINT REFERENCES teachers(id) ON DELETE SET NULL,
    group_id BIGINT REFERENCES groups(id) ON DELETE SET NULL,
    room_id BIGINT REFERENCES rooms(id) ON DELETE SET NULL,
    time_slot_id BIGINT REFERENCES time_slots(id) ON DELETE SET NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT chk_schedule_conflicts_type_not_blank CHECK (BTRIM(conflict_type) <> ''),
    CONSTRAINT chk_schedule_conflicts_severity CHECK (severity IN ('warning', 'error', 'critical'))
);

CREATE TABLE schedule (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    academic_period_id BIGINT NOT NULL REFERENCES academic_periods(id) ON DELETE RESTRICT,
    group_subject_load_id BIGINT NOT NULL REFERENCES group_subject_load(id) ON DELETE RESTRICT,
    group_id BIGINT NOT NULL REFERENCES groups(id) ON DELETE RESTRICT,
    subgroup_id BIGINT,
    subject_id BIGINT NOT NULL REFERENCES subjects(id) ON DELETE RESTRICT,
    lesson_type_id BIGINT NOT NULL REFERENCES lesson_types(id) ON DELETE RESTRICT,
    teacher_id BIGINT NOT NULL REFERENCES teachers(id) ON DELETE RESTRICT,
    room_id BIGINT NOT NULL REFERENCES rooms(id) ON DELETE RESTRICT,
    time_slot_id BIGINT NOT NULL REFERENCES time_slots(id) ON DELETE RESTRICT,
    source_run_id BIGINT REFERENCES schedule_generation_runs(id) ON DELETE SET NULL,
    status VARCHAR(20) NOT NULL DEFAULT 'planned',
    notes TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT fk_schedule_subgroup
        FOREIGN KEY (group_id, subgroup_id)
        REFERENCES group_subgroups(group_id, subgroup_id)
        ON DELETE RESTRICT,
    CONSTRAINT chk_schedule_status CHECK (status IN ('draft', 'planned', 'published', 'locked', 'cancelled'))
);

