-- ---------------------------------------------------------

WITH normalized_curriculum AS (
    SELECT
        REGEXP_REPLACE(UPPER(BTRIM(legacy_group_name)), '\s+', '', 'g') AS group_code,
        BTRIM(REGEXP_REPLACE(legacy_subject_name, '\s+', ' ', 'g')) AS subject_name,
        theory_hours,
        practice_hours,
        semester_no
    FROM stg_legacy_curriculum
),
lecture_load_rows AS (
    SELECT
        ap.academic_period_id,
        g.group_id,
        sb.subject_id,
        lt.lesson_type_id,
        nc.theory_hours::NUMERIC AS total_hours,
        ROUND((nc.theory_hours::NUMERIC / NULLIF(ap.weeks_in_period, 0)), 2) AS planned_weekly_hours,
        CASE
            WHEN sb.name ~* '(физическ|физкультур|дене\s*шынық|дене\s*тәрби)' THEN rt_gym.room_type_id
            WHEN sb.name ~* '(базы данных|sql|cms|big data|серверн|виртуализац|интерфейс|компьютерн.*граф|визуал|верстк|портфолио|интерактив|мобильн|веб[- ]?портал|информатик|икт|1с|программир|рефакторинг|микроконтроллер)' THEN rt_class.room_type_id
            WHEN sb.name ~* '(химия|биология|физика|микробиолог|санитар|пищев|сырье)' THEN rt_class.room_type_id
            WHEN sb.name ~* '(технология приготовления|технология хлеба|нан|макарон|пиво|спирт|безалкоголь|технохимическ|хлебное производство|оборудование|контроль)' THEN rt_class.room_type_id
            ELSE rt_class.room_type_id
        END AS preferred_room_type_id
    FROM normalized_curriculum nc
    JOIN groups g
      ON g.code = nc.group_code
    JOIN subjects sb
      ON sb.name = nc.subject_name
    JOIN academic_periods ap
      ON ap.code = FORMAT('LEGACY-S%s', nc.semester_no)
    JOIN lesson_types lt
      ON lt.code = 'LECTURE'
    CROSS JOIN LATERAL (SELECT room_type_id FROM room_types WHERE code = 'CLASSROOM') rt_class
    CROSS JOIN LATERAL (SELECT room_type_id FROM room_types WHERE code = 'GYM') rt_gym
    WHERE nc.theory_hours > 0
),
practice_load_rows AS (
    SELECT
        ap.academic_period_id,
        g.group_id,
        sb.subject_id,
        lt.lesson_type_id,
        nc.practice_hours::NUMERIC AS total_hours,
        ROUND((nc.practice_hours::NUMERIC / NULLIF(ap.weeks_in_period, 0)), 2) AS planned_weekly_hours,
        CASE
            WHEN sb.name ~* '(физическ|физкультур|дене\s*шынық|дене\s*тәрби)' THEN rt_gym.room_type_id
            WHEN sb.name ~* '(базы данных|sql|cms|big data|серверн|виртуализац|интерфейс|компьютерн.*граф|визуал|верстк|портфолио|интерактив|мобильн|веб[- ]?портал|информатик|икт|1с|программир|рефакторинг|микроконтроллер)' THEN rt_comp.room_type_id
            WHEN sb.name ~* '(химия|биология|физика|микробиолог|санитар|пищев|сырье)' THEN rt_science.room_type_id
            WHEN sb.name ~* '(технология приготовления|технология хлеба|нан|макарон|пиво|спирт|безалкоголь|технохимическ|хлебное производство|оборудование|контроль)' THEN rt_kitchen.room_type_id
            ELSE rt_class.room_type_id
        END AS preferred_room_type_id
    FROM normalized_curriculum nc
    JOIN groups g
      ON g.code = nc.group_code
    JOIN subjects sb
      ON sb.name = nc.subject_name
    JOIN academic_periods ap
      ON ap.code = FORMAT('LEGACY-S%s', nc.semester_no)
    JOIN lesson_types lt
      ON lt.code = 'PRACTICE'
    CROSS JOIN LATERAL (SELECT room_type_id FROM room_types WHERE code = 'CLASSROOM') rt_class
    CROSS JOIN LATERAL (SELECT room_type_id FROM room_types WHERE code = 'COMPUTER_LAB') rt_comp
    CROSS JOIN LATERAL (SELECT room_type_id FROM room_types WHERE code = 'SCIENCE_LAB') rt_science
    CROSS JOIN LATERAL (SELECT room_type_id FROM room_types WHERE code = 'KITCHEN_LAB') rt_kitchen
    CROSS JOIN LATERAL (SELECT room_type_id FROM room_types WHERE code = 'GYM') rt_gym
    WHERE nc.practice_hours > 0
),
all_load_rows AS (
    SELECT * FROM lecture_load_rows
    UNION ALL
    SELECT * FROM practice_load_rows
),
teacher_candidates AS (
    SELECT
        ts.subject_id,
        MIN(ts.teacher_id) AS single_teacher_id,
        COUNT(DISTINCT ts.teacher_id) AS teacher_count
    FROM teacher_subjects ts
    WHERE ts.is_active = TRUE
    GROUP BY ts.subject_id
)
INSERT INTO group_subject_load (
    academic_period_id,
    group_id,
    subgroup_id,
    subject_id,
    lesson_type_id,
    planned_weekly_hours,
    total_hours,
    min_sessions_per_week,
    max_sessions_per_week,
    preferred_room_type_id,
    preferred_teacher_id,
    is_mandatory,
    notes
)
SELECT
    alr.academic_period_id,
    alr.group_id,
    NULL::bigint,
    alr.subject_id,
    alr.lesson_type_id,
    alr.planned_weekly_hours,
    alr.total_hours,
    CASE WHEN alr.planned_weekly_hours >= 2 THEN 1 ELSE NULL END,
    NULL::smallint,
    alr.preferred_room_type_id,
    CASE WHEN tc.teacher_count = 1 THEN tc.single_teacher_id ELSE NULL END,
    TRUE,
    'Imported from legacy curriculum'
FROM all_load_rows alr
LEFT JOIN teacher_candidates tc
  ON tc.subject_id = alr.subject_id
ON CONFLICT DO NOTHING;

