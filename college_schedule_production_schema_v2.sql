            WHEN sb.name ~* '(базы данных|sql|cms|big data|серверн|виртуализац|интерфейс|компьютерн.*граф|визуал|верстк|портфолио|интерактив|мобильн|веб[- ]?портал|информатик|икт|1с|программир|рефакторинг|микроконтроллер)' THEN 'CLASSROOM'
            WHEN sb.name ~* '(химия|биология|физика|микробиолог|санитар|пищев|сырье)'
                 AND lt.code IN ('PRACTICE', 'LAB') THEN 'SCIENCE_LAB'
            WHEN sb.name ~* '(технология приготовления|технология хлеба|нан|макарон|пиво|спирт|безалкоголь|технохимическ|хлебное производство|оборудование|контроль)'
                 AND lt.code IN ('PRACTICE', 'LAB') THEN 'KITCHEN_LAB'
            ELSE 'CLASSROOM'
        END AS room_type_code,
        CASE
            WHEN sb.name ~* '(физическ|физкультур|дене\s*шынық|дене\s*тәрби)'
              OR (sb.name ~* '(базы данных|sql|cms|big data|серверн|виртуализац|интерфейс|компьютерн.*граф|визуал|верстк|портфолио|интерактив|мобильн|веб[- ]?портал|информатик|икт|1с|программир|рефакторинг|микроконтроллер)' AND lt.code IN ('PRACTICE', 'LAB'))
              OR (sb.name ~* '(химия|биология|физика|микробиолог|санитар|пищев|сырье)' AND lt.code IN ('PRACTICE', 'LAB'))
              OR (sb.name ~* '(технология приготовления|технология хлеба|нан|макарон|пиво|спирт|безалкоголь|технохимическ|хлебное производство|оборудование|контроль)' AND lt.code IN ('PRACTICE', 'LAB'))
            THEN TRUE
            ELSE TRUE
        END AS is_required
    FROM subjects sb
    JOIN subject_lesson_types slt
      ON slt.subject_id = sb.subject_id
     AND slt.is_active = TRUE
    JOIN lesson_types lt
      ON lt.lesson_type_id = slt.lesson_type_id
)
