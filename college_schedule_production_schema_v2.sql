CREATE OR REPLACE FUNCTION fn_limit_group_subgroups()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
    v_active_count INTEGER;
BEGIN
    IF NEW.is_active = FALSE THEN
        RETURN NEW;
    END IF;

    SELECT COUNT(*)
      INTO v_active_count
      FROM group_subgroups gs
     WHERE gs.group_id = NEW.group_id
       AND gs.is_active = TRUE
       AND gs.subgroup_id <> COALESCE(NEW.subgroup_id, -1);

    IF v_active_count >= 2 THEN
        RAISE EXCEPTION 'Group % can have at most 2 active subgroups', NEW.group_id;
    END IF;

    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION fn_schedule_validate_conflicts()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
    v_other_subgroups INTEGER;
BEGIN
    IF NEW.status = 'cancelled' THEN
        RETURN NEW;
    END IF;

    IF EXISTS (
        SELECT 1
          FROM schedule s
         WHERE s.schedule_id <> COALESCE(NEW.schedule_id, -1)
           AND s.status <> 'cancelled'
           AND s.academic_period_id = NEW.academic_period_id
           AND s.teacher_id = NEW.teacher_id
           AND s.time_slot_id = NEW.time_slot_id
    ) THEN
        RAISE EXCEPTION
            'Teacher % is already assigned to another lesson in time_slot % for academic_period %',
            NEW.teacher_id, NEW.time_slot_id, NEW.academic_period_id;
    END IF;

    IF EXISTS (
        SELECT 1
          FROM schedule s
         WHERE s.schedule_id <> COALESCE(NEW.schedule_id, -1)
           AND s.status <> 'cancelled'
           AND s.academic_period_id = NEW.academic_period_id
           AND s.room_id = NEW.room_id
           AND s.time_slot_id = NEW.time_slot_id
    ) THEN
        RAISE EXCEPTION
            'Room % is already occupied in time_slot % for academic_period %',
            NEW.room_id, NEW.time_slot_id, NEW.academic_period_id;
    END IF;

    IF NEW.subgroup_id IS NULL THEN
        IF EXISTS (
            SELECT 1
              FROM schedule s
             WHERE s.schedule_id <> COALESCE(NEW.schedule_id, -1)
               AND s.status <> 'cancelled'
               AND s.academic_period_id = NEW.academic_period_id
               AND s.group_id = NEW.group_id
               AND s.time_slot_id = NEW.time_slot_id
        ) THEN
            RAISE EXCEPTION
                'Group % already has a lesson in time_slot % for academic_period %',
                NEW.group_id, NEW.time_slot_id, NEW.academic_period_id;
        END IF;
    ELSE
        IF EXISTS (
            SELECT 1
              FROM schedule s
             WHERE s.schedule_id <> COALESCE(NEW.schedule_id, -1)
               AND s.status <> 'cancelled'
               AND s.academic_period_id = NEW.academic_period_id
               AND s.group_id = NEW.group_id
               AND s.time_slot_id = NEW.time_slot_id
               AND s.subgroup_id IS NULL
        ) THEN
            RAISE EXCEPTION
                'Full group % is already scheduled in time_slot % for academic_period %',
                NEW.group_id, NEW.time_slot_id, NEW.academic_period_id;
        END IF;

        IF EXISTS (
            SELECT 1
              FROM schedule s
             WHERE s.schedule_id <> COALESCE(NEW.schedule_id, -1)
               AND s.status <> 'cancelled'
               AND s.academic_period_id = NEW.academic_period_id
               AND s.group_id = NEW.group_id
               AND s.time_slot_id = NEW.time_slot_id
               AND s.subgroup_id = NEW.subgroup_id
        ) THEN
            RAISE EXCEPTION
                'Subgroup % of group % already has a lesson in time_slot % for academic_period %',
                NEW.subgroup_id, NEW.group_id, NEW.time_slot_id, NEW.academic_period_id;
        END IF;

        SELECT COUNT(DISTINCT s.subgroup_id)
          INTO v_other_subgroups
          FROM schedule s
         WHERE s.schedule_id <> COALESCE(NEW.schedule_id, -1)
           AND s.status <> 'cancelled'
           AND s.academic_period_id = NEW.academic_period_id
           AND s.group_id = NEW.group_id
           AND s.time_slot_id = NEW.time_slot_id
           AND s.subgroup_id IS NOT NULL
           AND s.subgroup_id <> NEW.subgroup_id;

        IF v_other_subgroups >= 2 THEN
            RAISE EXCEPTION
                'Group % cannot be split into more than 2 parallel subgroups in the same time slot',
                NEW.group_id;
        END IF;
    END IF;

    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION fn_schedule_validate_assignment()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
    v_load RECORD;
    v_teacher_active BOOLEAN;
    v_group_active BOOLEAN;
    v_room_active BOOLEAN;
    v_slot_active BOOLEAN;
    v_room_type_id BIGINT;
    v_room_is_lab BOOLEAN;
    v_room_capacity INTEGER;
    v_required_capacity INTEGER;
    v_lesson_code VARCHAR(20);
BEGIN
    IF NEW.status = 'cancelled' THEN
        RETURN NEW;
    END IF;

    SELECT gsl.academic_period_id,
           gsl.group_id,
           gsl.subgroup_id,
           gsl.subject_id,
           gsl.lesson_type_id
      INTO v_load
      FROM group_subject_load gsl
     WHERE gsl.group_subject_load_id = NEW.group_subject_load_id;

    IF NOT FOUND THEN
        RAISE EXCEPTION 'group_subject_load % does not exist', NEW.group_subject_load_id;
    END IF;

    IF v_load.academic_period_id <> NEW.academic_period_id
       OR v_load.group_id <> NEW.group_id
       OR v_load.subject_id <> NEW.subject_id
       OR v_load.lesson_type_id <> NEW.lesson_type_id
       OR v_load.subgroup_id IS DISTINCT FROM NEW.subgroup_id THEN
        RAISE EXCEPTION
            'schedule row does not match group_subject_load %', NEW.group_subject_load_id;
    END IF;

    SELECT is_active INTO v_teacher_active
      FROM teachers
     WHERE teacher_id = NEW.teacher_id;

    SELECT is_active INTO v_group_active
      FROM groups
     WHERE group_id = NEW.group_id;

    SELECT r.is_active, r.room_type_id, rt.is_lab, r.capacity
      INTO v_room_active, v_room_type_id, v_room_is_lab, v_room_capacity
      FROM rooms r
      JOIN room_types rt ON rt.room_type_id = r.room_type_id
     WHERE r.room_id = NEW.room_id;

    SELECT is_active INTO v_slot_active
      FROM time_slots
     WHERE time_slot_id = NEW.time_slot_id;

    IF v_teacher_active IS DISTINCT FROM TRUE THEN
        RAISE EXCEPTION 'Teacher % is inactive and cannot be scheduled', NEW.teacher_id;
    END IF;

    IF v_group_active IS DISTINCT FROM TRUE THEN
        RAISE EXCEPTION 'Group % is inactive and cannot be scheduled', NEW.group_id;
    END IF;

    IF v_room_active IS DISTINCT FROM TRUE THEN
        RAISE EXCEPTION 'Room % is inactive and cannot be scheduled', NEW.room_id;
    END IF;

    IF v_slot_active IS DISTINCT FROM TRUE THEN
        RAISE EXCEPTION 'Time slot % is inactive and cannot be scheduled', NEW.time_slot_id;
    END IF;

    IF NEW.subgroup_id IS NOT NULL THEN
        PERFORM 1
          FROM group_subgroups gs
         WHERE gs.group_id = NEW.group_id
           AND gs.subgroup_id = NEW.subgroup_id
           AND gs.is_active = TRUE;

        IF NOT FOUND THEN
            RAISE EXCEPTION
                'Subgroup % is not active or does not belong to group %',
                NEW.subgroup_id, NEW.group_id;
        END IF;
    END IF;

    IF NOT EXISTS (
        SELECT 1
          FROM teacher_subjects ts
         WHERE ts.teacher_id = NEW.teacher_id
           AND ts.subject_id = NEW.subject_id
           AND ts.is_active = TRUE
           AND (ts.lesson_type_id IS NULL OR ts.lesson_type_id = NEW.lesson_type_id)
    ) THEN
        RAISE EXCEPTION
            'Teacher % is not allowed to teach subject % with lesson_type %',
            NEW.teacher_id, NEW.subject_id, NEW.lesson_type_id;
    END IF;

    IF EXISTS (
        SELECT 1
          FROM subject_lesson_types slt
         WHERE slt.subject_id = NEW.subject_id
           AND slt.is_active = TRUE
    )
    AND NOT EXISTS (
        SELECT 1
          FROM subject_lesson_types slt
         WHERE slt.subject_id = NEW.subject_id
           AND slt.lesson_type_id = NEW.lesson_type_id
           AND slt.is_active = TRUE
    ) THEN
        RAISE EXCEPTION
            'Subject % does not allow lesson_type %',
            NEW.subject_id, NEW.lesson_type_id;
    END IF;

    SELECT code
      INTO v_lesson_code
      FROM lesson_types
     WHERE lesson_type_id = NEW.lesson_type_id;

    IF v_lesson_code = 'LAB' AND COALESCE(v_room_is_lab, FALSE) = FALSE THEN
        RAISE EXCEPTION
            'Lesson type LAB can only be scheduled in a room with is_lab = TRUE';
    END IF;

    IF EXISTS (
        SELECT 1
          FROM subject_room_type srt
         WHERE srt.subject_id = NEW.subject_id
           AND srt.lesson_type_id = NEW.lesson_type_id
           AND srt.is_required = TRUE
    ) THEN
        IF NOT EXISTS (
            SELECT 1
              FROM subject_room_type srt
             WHERE srt.subject_id = NEW.subject_id
               AND srt.lesson_type_id = NEW.lesson_type_id
               AND srt.room_type_id = v_room_type_id
               AND srt.is_required = TRUE
        ) THEN
            RAISE EXCEPTION
                'Room % has incompatible room_type for subject % and lesson_type %',
                NEW.room_id, NEW.subject_id, NEW.lesson_type_id;
        END IF;
    ELSIF EXISTS (
        SELECT 1
          FROM subject_room_type srt
         WHERE srt.subject_id = NEW.subject_id
           AND srt.lesson_type_id IS NULL
           AND srt.is_required = TRUE
    ) THEN
        IF NOT EXISTS (
            SELECT 1
              FROM subject_room_type srt
             WHERE srt.subject_id = NEW.subject_id
               AND srt.lesson_type_id IS NULL
               AND srt.room_type_id = v_room_type_id
               AND srt.is_required = TRUE
        ) THEN
            RAISE EXCEPTION
                'Room % has incompatible room_type for subject %',
                NEW.room_id, NEW.subject_id;
        END IF;
    END IF;

    SELECT COALESCE(gs.student_count, g.student_count)
      INTO v_required_capacity
      FROM groups g
      LEFT JOIN group_subgroups gs
        ON gs.group_id = g.group_id
       AND gs.subgroup_id = NEW.subgroup_id
     WHERE g.group_id = NEW.group_id;

    IF v_room_capacity IS NOT NULL
       AND v_required_capacity IS NOT NULL
       AND v_room_capacity < v_required_capacity THEN
        RAISE EXCEPTION
            'Room % capacity (%) is lower than required student count (%)',
            NEW.room_id, v_room_capacity, v_required_capacity;
    END IF;

    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION fn_schedule_validate_availability()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
    IF NEW.status = 'cancelled' THEN
        RETURN NEW;
    END IF;

    IF EXISTS (
        SELECT 1
          FROM teacher_unavailability tu
         WHERE tu.teacher_id = NEW.teacher_id
           AND tu.academic_period_id = NEW.academic_period_id
           AND tu.time_slot_id = NEW.time_slot_id
           AND tu.is_hard = TRUE
    ) THEN
        RAISE EXCEPTION
            'Teacher % is unavailable in time_slot % for academic_period %',
            NEW.teacher_id, NEW.time_slot_id, NEW.academic_period_id;
    END IF;

    IF EXISTS (
        SELECT 1
          FROM room_unavailability ru
         WHERE ru.room_id = NEW.room_id
           AND ru.academic_period_id = NEW.academic_period_id
           AND ru.time_slot_id = NEW.time_slot_id
           AND ru.is_hard = TRUE
    ) THEN
        RAISE EXCEPTION
            'Room % is unavailable in time_slot % for academic_period %',
            NEW.room_id, NEW.time_slot_id, NEW.academic_period_id;
    END IF;

    IF NEW.subgroup_id IS NULL THEN
        IF EXISTS (
            SELECT 1
              FROM group_unavailability gu
             WHERE gu.group_id = NEW.group_id
               AND gu.academic_period_id = NEW.academic_period_id
               AND gu.time_slot_id = NEW.time_slot_id
               AND gu.is_hard = TRUE
        ) THEN
            RAISE EXCEPTION
                'Group % is unavailable in time_slot % for academic_period %',
                NEW.group_id, NEW.time_slot_id, NEW.academic_period_id;
        END IF;
    ELSE
        IF EXISTS (
            SELECT 1
              FROM group_unavailability gu
             WHERE gu.group_id = NEW.group_id
               AND gu.academic_period_id = NEW.academic_period_id
               AND gu.time_slot_id = NEW.time_slot_id
               AND gu.is_hard = TRUE
               AND (gu.subgroup_id IS NULL OR gu.subgroup_id = NEW.subgroup_id)
        ) THEN
            RAISE EXCEPTION
                'Group % / subgroup % is unavailable in time_slot % for academic_period %',
                NEW.group_id, NEW.subgroup_id, NEW.time_slot_id, NEW.academic_period_id;
        END IF;
    END IF;

    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION fn_schedule_validate_loads()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
    v_day_of_week SMALLINT;
    v_slot_hours NUMERIC(6,2);
    v_period_weeks NUMERIC(6,2);
    v_period_start DATE;
    v_period_end DATE;
    v_group_max_daily_lessons SMALLINT;
    v_load_weekly_hours NUMERIC(8,2);
    v_load_total_hours NUMERIC(8,2);
    v_load_max_sessions_per_week SMALLINT;
    v_group_daily_slots INTEGER;
    v_assigned_load_hours NUMERIC(8,2);
    v_assigned_load_sessions INTEGER;
    v_teacher_weekly_hours NUMERIC(8,2);
    v_teacher_daily_hours NUMERIC(8,2);
    v_teacher_daily_lessons INTEGER;
    v_max_weekly_hours NUMERIC(8,2);
    v_max_daily_hours NUMERIC(8,2);
    v_max_monthly_hours NUMERIC(8,2);
    v_max_daily_lessons SMALLINT;
    v_period_days INTEGER;
    v_period_months NUMERIC(10,4);
    v_teacher_average_monthly_hours NUMERIC(10,2);
BEGIN
    IF NEW.status = 'cancelled' THEN
        RETURN NEW;
    END IF;

    SELECT ts.day_of_week, ts.academic_hours
      INTO v_day_of_week, v_slot_hours
      FROM time_slots ts
     WHERE ts.time_slot_id = NEW.time_slot_id;

    SELECT ap.weeks_in_period, ap.start_date, ap.end_date
      INTO v_period_weeks, v_period_start, v_period_end
      FROM academic_periods ap
     WHERE ap.academic_period_id = NEW.academic_period_id;

    SELECT g.max_daily_lessons
      INTO v_group_max_daily_lessons
      FROM groups g
     WHERE g.group_id = NEW.group_id;

    SELECT gsl.planned_weekly_hours,
           gsl.total_hours,
           gsl.max_sessions_per_week
      INTO v_load_weekly_hours,
           v_load_total_hours,
           v_load_max_sessions_per_week
      FROM group_subject_load gsl
     WHERE gsl.group_subject_load_id = NEW.group_subject_load_id;

    SELECT COUNT(*)
      INTO v_group_daily_slots
      FROM (
            SELECT DISTINCT s.time_slot_id
              FROM schedule s
              JOIN time_slots ts ON ts.time_slot_id = s.time_slot_id
             WHERE s.schedule_id <> COALESCE(NEW.schedule_id, -1)
               AND s.status <> 'cancelled'
               AND s.academic_period_id = NEW.academic_period_id
               AND s.group_id = NEW.group_id
               AND ts.day_of_week = v_day_of_week
            UNION
            SELECT NEW.time_slot_id
      ) AS daily_slots;

    IF v_group_daily_slots > v_group_max_daily_lessons THEN
        RAISE EXCEPTION
            'Group % would exceed its daily limit of % lessons',
            NEW.group_id, v_group_max_daily_lessons;
    END IF;

    SELECT COALESCE(SUM(ts.academic_hours), 0),
           COUNT(*)
      INTO v_assigned_load_hours,
           v_assigned_load_sessions
      FROM schedule s
      JOIN time_slots ts ON ts.time_slot_id = s.time_slot_id
     WHERE s.schedule_id <> COALESCE(NEW.schedule_id, -1)
       AND s.status <> 'cancelled'
       AND s.group_subject_load_id = NEW.group_subject_load_id;

    v_assigned_load_hours := v_assigned_load_hours + v_slot_hours;
    v_assigned_load_sessions := v_assigned_load_sessions + 1;

    IF v_load_weekly_hours > 0
       AND v_assigned_load_hours > v_load_weekly_hours + 0.01 THEN
        RAISE EXCEPTION
            'group_subject_load % exceeds planned weekly hours (scheduled %, limit %)',
            NEW.group_subject_load_id, v_assigned_load_hours, v_load_weekly_hours;
    END IF;

    IF v_load_total_hours > 0
       AND v_period_weeks > 0
       AND (v_assigned_load_hours * v_period_weeks) > v_load_total_hours + 0.01 THEN
        RAISE EXCEPTION
            'group_subject_load % exceeds total period hours (projected %, limit %)',
            NEW.group_subject_load_id, (v_assigned_load_hours * v_period_weeks), v_load_total_hours;
    END IF;

    IF v_load_max_sessions_per_week IS NOT NULL
       AND v_assigned_load_sessions > v_load_max_sessions_per_week THEN
        RAISE EXCEPTION
            'group_subject_load % exceeds max sessions per week (% > %)',
            NEW.group_subject_load_id, v_assigned_load_sessions, v_load_max_sessions_per_week;
    END IF;

    SELECT COALESCE(tl.max_weekly_hours, t.default_weekly_hour_limit),
           COALESCE(tl.max_daily_hours, t.default_daily_hour_limit),
           COALESCE(tl.max_monthly_hours, t.default_monthly_hour_limit),
           COALESCE(tl.max_daily_lessons, t.default_daily_lessons_limit)
      INTO v_max_weekly_hours,
           v_max_daily_hours,
           v_max_monthly_hours,
           v_max_daily_lessons
      FROM teachers t
      LEFT JOIN teacher_load tl
        ON tl.teacher_id = t.teacher_id
       AND tl.academic_period_id = NEW.academic_period_id
     WHERE t.teacher_id = NEW.teacher_id;

    SELECT COALESCE(SUM(ts.academic_hours), 0)
      INTO v_teacher_weekly_hours
      FROM schedule s
      JOIN time_slots ts ON ts.time_slot_id = s.time_slot_id
     WHERE s.schedule_id <> COALESCE(NEW.schedule_id, -1)
       AND s.status <> 'cancelled'
       AND s.academic_period_id = NEW.academic_period_id
       AND s.teacher_id = NEW.teacher_id;

    v_teacher_weekly_hours := v_teacher_weekly_hours + v_slot_hours;

    SELECT COALESCE(SUM(ts.academic_hours), 0),
           COUNT(*)
      INTO v_teacher_daily_hours,
           v_teacher_daily_lessons
      FROM schedule s
      JOIN time_slots ts ON ts.time_slot_id = s.time_slot_id
     WHERE s.schedule_id <> COALESCE(NEW.schedule_id, -1)
       AND s.status <> 'cancelled'
       AND s.academic_period_id = NEW.academic_period_id
       AND s.teacher_id = NEW.teacher_id
       AND ts.day_of_week = v_day_of_week;

    v_teacher_daily_hours := v_teacher_daily_hours + v_slot_hours;
    v_teacher_daily_lessons := v_teacher_daily_lessons + 1;

    IF v_max_weekly_hours IS NOT NULL
       AND v_teacher_weekly_hours > v_max_weekly_hours + 0.01 THEN
        RAISE EXCEPTION
            'Teacher % exceeds weekly hour limit (scheduled %, limit %)',
            NEW.teacher_id, v_teacher_weekly_hours, v_max_weekly_hours;
    END IF;

    IF v_max_daily_hours IS NOT NULL
       AND v_teacher_daily_hours > v_max_daily_hours + 0.01 THEN
        RAISE EXCEPTION
            'Teacher % exceeds daily hour limit (scheduled %, limit %)',
            NEW.teacher_id, v_teacher_daily_hours, v_max_daily_hours;
    END IF;

    IF v_max_daily_lessons IS NOT NULL
       AND v_teacher_daily_lessons > v_max_daily_lessons THEN
        RAISE EXCEPTION
            'Teacher % exceeds daily lesson limit (scheduled %, limit %)',
            NEW.teacher_id, v_teacher_daily_lessons, v_max_daily_lessons;
    END IF;

    IF v_max_monthly_hours IS NOT NULL THEN
        v_period_days := GREATEST(1, (v_period_end - v_period_start + 1));
        v_period_months := GREATEST(1.0::NUMERIC, v_period_days::NUMERIC / 30.4375);
        v_teacher_average_monthly_hours := (v_teacher_weekly_hours * v_period_weeks) / v_period_months;

        IF v_teacher_average_monthly_hours > v_max_monthly_hours + 0.01 THEN
            RAISE EXCEPTION
                'Teacher % exceeds average monthly hour limit (projected %, limit %)',
